package Exercicios_vetores;

public class alturas_class {

	private String name;
	private int old;
	private double height;
	
	public alturas_class (String name,int old,double height) {
		
		this.name=name;
		this.old=old;
		this.height=height;
	}

	public String getName() {
		return name;
	}


	public int getOld() {
		return old;
	}

	public double getHeight() {
		return height;
	}
	
	

	
	
}
